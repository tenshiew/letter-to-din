async function decryptData(password) {
    const salt = new Uint8Array(encryptedPayload.salt);
    const iv = new Uint8Array(encryptedPayload.iv);
    const data = new Uint8Array(encryptedPayload.data);

    const enc = new TextEncoder();
    const dec = new TextDecoder();

    const keyMaterial = await crypto.subtle.importKey(
        "raw", enc.encode(password), "PBKDF2", false, ["deriveKey"]
    );

    const key = await crypto.subtle.deriveKey({
        name: "PBKDF2",
        salt,
        iterations: 100000,
        hash: "SHA-256"
        }, keyMaterial, {
        name: "AES-GCM",
        length: 256
    }, false, ["decrypt"]);

    try {
        const decrypted = await crypto.subtle.decrypt(
            { name: "AES-GCM", iv },
            key,
            data
        );
        return dec.decode(decrypted);
    } catch (e) {
        return null;
    }
}

document.getElementById("decryptBtn").onclick = async () => {
    const password = document.getElementById("password").value;
    const out = document.getElementById("output");

    const result = await decryptData(password);

    if (result) out.innerHTML = result;
    else out.textContent = " ";
};